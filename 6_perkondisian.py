# Contoh if else if

nilai = 100

if(nilai >= 10 and nilai < 10):
    print("Mendapat Nilai A")
elif(nilai > 7 and nilai < 10):
    print("Mendapat Nilai B")
elif(nilai >= 5 and nilai <= 7):
    print("Mendapat Nilai C")
else:
    print("Masukkan Nilai 1 - 10")
