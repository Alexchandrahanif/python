# List Disini sama saja dengan array

# 
nama = ["Alex", "Chandra", "Hanif"]

print(nama)

# Menambahkan Nilai
nama.append("Ganteng")

print(nama)

# Menghapus Nilai
nama.remove("Hanif")

print(nama)

# Mengubah Nilai
nama[1] = "Chandra Hanif" # untuk index nya sama dengan array mulai dari 0

print(nama)