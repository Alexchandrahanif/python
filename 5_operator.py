# disini kita sekalian berkenalan dengan type data Boolean
# pada boolean ada 2, yaitu true, dan false

# Pada Operator ada 2 istilah, yaitu and / or

# and (dan) => akan bernilai true ketika kondisi nya harus terpenuhi semua
# or (atau) => akan bernilai true ketika terpenuhi salah satu


print(True) 
print(False)

print(True and True) #True
print(False and True) #False
print(False and False) #False

print(True or True) #True
print(False or True) #True
print(False or False) #False


